public class road {
	
	int Id_r;
	public String info;
	
	public road (int i, String k) {
		
		this.Id_r = i;
		this.info = k;
	}

	public int getId_r() {
		return Id_r;
	}

	public void setId_r(int id_r) {
		Id_r = id_r;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}
	
	
}
